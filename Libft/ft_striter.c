#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	size_t i;
	size_t n;

	if (!s || !f)
		return ;
	i = 0;
	n = ft_strlen(s);
	while (i < n)
		f(&s[i++]);
}
