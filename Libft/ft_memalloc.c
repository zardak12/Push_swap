#include "libft.h"

void	*ft_memalloc(size_t size)
{
	unsigned char *ret;

	if (!(ret = (unsigned char*)malloc(sizeof(*ret) * size)))
		return (NULL);
	ft_bzero(ret, size);
	return ((void*)ret);
}
