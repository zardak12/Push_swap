#include "libft.h"

static void	ft_fallback(t_list **lst)
{
	t_list *tlst;
	t_list *temp;

	tlst = *lst;
	while (tlst)
	{
		temp = tlst;
		tlst = tlst->next;
		free(temp);
	}
}

t_list		*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list		*new;
	t_list		*list;

	if (!lst)
		return (NULL);
	list = f(lst);
	new = list;
	while (lst->next)
	{
		lst = lst->next;
		if (!(list->next = f(lst)))
		{
			ft_fallback(&new);
			return (NULL);
		}
		list = list->next;
	}
	return (new);
}
